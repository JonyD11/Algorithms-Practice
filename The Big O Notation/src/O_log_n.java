
public class O_log_n {

	/*
	 * What is O(log n)?
	 * O(log n) basically means time goes up linearly while the n
	 * goes up exponentially. So if it takes 1 second to compute
	 * 10 elements, it will take 2 seconds to compute 100 elements,
	 * 3 seconds to compute 1000 elements, and so on. It is O(log n)
	 * when we do divide and conquer type of algorithms e.g binary
	 * search.
	 * 
	 * */
	
	
	/*
	 * What is the comparison between O(log n) & O(n)?
	 * If we compare the linear growth with the logarithmic curve
	 * as we can see linear curve grows at the same rate but the
	 * logarithmic curve slows down at some point. So, the 
	 * algorithm that runs in logarithmic time is more efficient
	 * and more scalable than the algorithm time that runs on 
	 * linear and quadratic time. 
	 * */
}
